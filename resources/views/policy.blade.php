<strong>&iquest;Qu&eacute; son las cookies?</strong></p>
<p style="text-align:justify;">Las cookies son peque&ntilde;os archivos de texto que el sitio web almacena en el ordenador o dispositivo m&oacute;vil del usuario a trav&eacute;s del navegador de Internet. El uso de cookies permite, por ejemplo, reconocer el dispositivo del usuario en una visita posterior o entender qu&eacute; productos o servicios le interesan m&aacute;s.</p>

<br>

<p style="text-align:justify;"><strong>&iquest;Para qu&eacute; se utilizan las cookies?</strong></p>
<p style="text-align:justify;">Las cookies se utilizan para gestionar el funcionamiento de los sitios web, es decir, para aumentar su eficacia y permitir a los propietarios de los sitios web recopilar informaci&oacute;n y preferencias de los usuarios. De este modo, el almacenamiento de cookies permite una navegaci&oacute;n m&aacute;s r&aacute;pida y eficiente, eliminando la necesidad de introducir repetidamente la misma informaci&oacute;n.</p>
	
